async function readEventDeclarations(source: string): Promise<Record<string, Record<string, EventFields>>> {
  const contents = source.startsWith('argonprotocol/') ? await fetchGitSource(source) : await fetchNpmSource(source);
  const sourceFile = ts.createSourceFile('augment-api-events.ts', contents, ts.ScriptTarget.Latest, true);
  const eventsInterface = findEventsInterface(sourceFile);
  if (!eventsInterface) throw new Error(`Unable to find AugmentedEvents in ${source}`);

  const result: Record<string, Record<string, EventFields>> = {};
  for (const sectionMember of eventsInterface.members) {
    if (!ts.isPropertySignature(sectionMember) || !sectionMember.type || !ts.isTypeLiteralNode(sectionMember.type))
      continue;
    const section = getPropertyName(sectionMember.name);
    if (!section || !includedSections.has(section)) continue;

    const methods = (result[section] ??= {});
    for (const methodMember of sectionMember.type.members) {
      if (!ts.isPropertySignature(methodMember) || !methodMember.type || !ts.isTypeReferenceNode(methodMember.type)) {
        continue;
      }
      const method = getPropertyName(methodMember.name);
      const tuple = methodMember.type.typeArguments?.[1];
      if (!method || !tuple || !ts.isTupleTypeNode(tuple)) continue;

      const fields: EventFields = {};
      for (const [index, element] of tuple.elements.entries()) {
        if (ts.isNamedTupleMember(element)) {
          fields[element.name.text] = element.type.getText(sourceFile);
        } else {
          fields[index] = element.getText(sourceFile);
        }
      }
      methods[method] = fields;
    }
  }
  return result;
}

async function fetchNpmSource(version: string): Promise<string> {
  const response = await fetch(`https://registry.npmjs.org/@argonprotocol/mainchain/-/mainchain-${version}.tgz`);
  if (!response.ok) throw new Error(`Unable to download ${npmPackage}@${version}: ${response.status}`);

  const archive = gunzipSync(Buffer.from(await response.arrayBuffer()));
  return (
    readTarFile(archive, npmPackagePath) ??
    readTarFile(archive, npmDeclarationPath) ??
    readTarFile(archive, npmBundleDeclarationPath) ??
    failMissingSource(version)
  );
}

async function fetchGitSource(source: string): Promise<string> {
  const commit = source.slice(source.lastIndexOf('@') + 1);
  const url = `https://raw.githubusercontent.com/argonprotocol/mainchain/${commit}/client/nodejs/src/interfaces/augment-api-events.ts`;
  const response = await fetch(url);
  if (!response.ok) throw new Error(`Unable to download ${source}: ${response.status}`);
  return response.text();
}

function readTarFile(archive: Buffer, requestedPath: string): string | undefined {
  for (let offset = 0; offset < archive.length; ) {
    const name = archive
      .subarray(offset, offset + 100)
      .toString()
      .replace(/\0.*$/, '');
    if (!name) return;

    const size = Number.parseInt(
      archive
        .subarray(offset + 124, offset + 136)
        .toString()
        .replace(/\0.*$/, '')
        .trim(),
      8,
    );
    const contentsOffset = offset + 512;
    if (name === requestedPath) return archive.subarray(contentsOffset, contentsOffset + size).toString();
    offset = contentsOffset + Math.ceil(size / 512) * 512;
  }
}

function findEventsInterface(sourceFile: ts.SourceFile): ts.InterfaceDeclaration | undefined {
  let result: ts.InterfaceDeclaration | undefined;
  sourceFile.forEachChild(function visit(node) {
    if (ts.isInterfaceDeclaration(node) && node.name.text === 'AugmentedEvents') result = node;
    if (!result) node.forEachChild(visit);
  });
  return result;
}

function getPropertyName(name: ts.PropertyName): string | undefined {
  if (ts.isIdentifier(name) || ts.isStringLiteral(name) || ts.isNumericLiteral(name)) return name.text;
}

function failMissingSource(version: string): never {
  throw new Error(`${npmPackage}@${version} does not include an AugmentedEvents declaration`);
}
