type EventFields = Record<string, string>;
type EventChanges = { spec: number; section: string; method: string; fields: EventFields | null }[];

const changes: EventChanges = [];
const sourceLabels: Record<number, string> = {};
let previousDeclarations: Record<string, Record<string, EventFields[]>> = {};

for (const [rawSpecVersion, sourceVersions] of Object.entries(sources)) {
  const specVersion = Number(rawSpecVersion);
  sourceLabels[specVersion] = sourceVersions
    .map(source => (source.startsWith('argonprotocol/') ? source : `${npmPackage}@${source}`))
    .join(', ');

  const declarationsForSpec: Record<string, Record<string, EventFields[]>> = {};
  const declarationsAtSpec = await Promise.all(sourceVersions.map(readEventDeclarations));
  for (const packageDeclarations of declarationsAtSpec) {
    for (const [section, methods] of Object.entries(packageDeclarations)) {
      for (const [method, fields] of Object.entries(methods)) {
        const alternatives = ((declarationsForSpec[section] ??= {})[method] ??= []);
        if (!alternatives.some(candidate => JSON.stringify(candidate) === JSON.stringify(fields)))
          alternatives.push(fields);
      }
    }
  }

  const sections = new Set([...Object.keys(previousDeclarations), ...Object.keys(declarationsForSpec)]);
  for (const section of sections) {
    const previousMethods = previousDeclarations[section] ?? {};
    const currentMethods = declarationsForSpec[section] ?? {};
    const methods = new Set([...Object.keys(previousMethods), ...Object.keys(currentMethods)]);

    for (const method of methods) {
      const previousAlternatives = previousMethods[method] ?? [];
      const currentAlternatives = currentMethods[method] ?? [];
      if (JSON.stringify(previousAlternatives) === JSON.stringify(currentAlternatives)) continue;

      if (!currentAlternatives.length) {
        changes.push({ spec: specVersion, section, method, fields: null });
      } else {
        for (const fields of currentAlternatives) changes.push({ spec: specVersion, section, method, fields });
      }
    }
  }
  previousDeclarations = declarationsForSpec;
}

